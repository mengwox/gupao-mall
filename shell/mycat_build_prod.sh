#mycat 生产启动配置
java -server \
  -Xms10240m \
  -Xmx10240m \
  -XX:MaxMetaspaceSize=1024m \
  -XX:MetaspaceSize=1024m \
  -XX:+UseConcMarkSweepGC \
  -XX:CMSInitiatingOccupancyFraction=75 \
  -XX:+CMSClassUnloadingEnabled \
  -XX:+UseCMSInitiatingOccupancyOnly \
  -XX:+CMSParallelRemarkEnabled \
  -XX:+CMSScavengeBeforeRemark \
  -XX:+ExplicitGCInvokesConcurrent \
  -XX:ErrorFile=/dump/hs_err.log \
  -XX:OnError=/home/xxxxx/server/mycat/jvmOnError.sh \
  -XX:+HeapDumpOnOutOfMemoryError \
  -XX:HeapDumpPath=/home/wq/server/mycat/dump.hprof \
  -XX:OnOutOfMemoryError=/home/wq/server/mycat/dump.sh \
  -XX:+PrintGCDetails \
  -XX:+PrintGCDateStamps \
  -Xdebug \
  -Xrunjdwp:server=y,transport=dt_socket,address=35456,suspend=n \
  -DMYCAT_HOME=/home/wq/server/mycat/resources \
  -Dcom.sun.management.jmxremote \
  -Dcom.sun.management.jmxremote.authenticate=false \
  -Dcom.sun.management.jmxremote.ssl=false \
  -Dcom.sun.management.jmxremote.port=6375 \
  -Dlogback.configurationFile=/mnt/wq/server/mycat/resources/logback.xml \
  -jar /home/xxxxx/server/mycat/mycat2-1.20-jar-with-dependencies.jar
